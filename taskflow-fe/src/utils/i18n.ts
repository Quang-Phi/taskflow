export const getLanguage = (): Language => {
  return (localStorage.getItem('taskflow_lang') as Language) || 'vi';
};

export const setLanguage = (lang: Language) => {
  localStorage.setItem('taskflow_lang', lang);
  window.dispatchEvent(new Event('language-changed'));
};

export const useTranslation = () => {
  const [lang, setLangState] = useState<Language>(getLanguage);

  useEffect(() => {
    const handleLangChange = () => {
      setLangState(getLanguage());
    };
    window.addEventListener('language-changed', handleLangChange);
    window.addEventListener('storage', handleLangChange);
    return () => {
      window.removeEventListener('language-changed', handleLangChange);
      window.removeEventListener('storage', handleLangChange);
    };
  }, []);

  const t = (key: keyof typeof translations['vi'], params?: Record<string, string | number>): string => {
    const dict = translations[lang] || translations['vi'];
    let text = dict[key] || translations['vi'][key] || key;
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        text = text.replace(`{${k}}`, String(v));
      });
    }
    return text;
  };

  return { t, lang, changeLanguage: setLanguage };
};
